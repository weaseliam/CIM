CREATE TABLE titular (
	id BIGINT GENERATED BY DEFAULT AS IDENTITY,
	cnp VARCHAR(32),
	nume VARCHAR(128) NOT NULL,
	prenume VARCHAR(128) NOT NULL,
	codloc INT,
	localitate VARCHAR(128),
	judet VARCHAR(64),
	tara VARCHAR(64),
	str VARCHAR(128),
	nr VARCHAR(32),
	bl VARCHAR(32),
	sc VARCHAR(32),
	et VARCHAR(32),
	ap VARCHAR(32),
	codpost VARCHAR(64),
	tel VARCHAR(64),
	fax VARCHAR(64),
	mail VARCHAR(128),
	nr_contr VARCHAR(64),
	data_contr DATE,
	contract VARCHAR(64),
	act VARCHAR(32),
	seria VARCHAR(32),
	nr_act VARCHAR(64),
	emitator VARCHAR(128),
	data_act DATE,
	dubiu VARCHAR(64),
	avans VARCHAR(64),
	an_debut INT,
	an_final INT,
	modif VARCHAR(64),
	avizat VARCHAR(64),
	data_aviz DATE
);

CREATE TABLE cimitir (
	id BIGINT GENERATED BY DEFAULT AS IDENTITY,
	nume VARCHAR(128) NOT NULL,
	localitate VARCHAR(128) NOT NULL,
	judet VARCHAR(64) NOT NULL,
	tara VARCHAR(64) NOT NULL,
	str VARCHAR(128) NOT NULL,
	nr VARCHAR(32) NOT NULL
);

CREATE TABLE scutit (
	id BIGINT GENERATED BY DEFAULT AS IDENTITY,
	nume VARCHAR(128) NOT NULL
);

CREATE TABLE loc (
	id BIGINT GENERATED BY DEFAULT AS IDENTITY,
	titular_id BIGINT NOT NULL,
	cimitir_id BIGINT NOT NULL,
	codzona INT,
	sector VARCHAR(32),
	rind VARCHAR(32),
	pozitie VARCHAR(32),
	nrlocuri INT,
	ani INT,
	dataincep DATE,
	dataexp DATE,
	scutit_id BIGINT NOT NULL,
	nr_ch VARCHAR(32),
	data_ch DATE,
	suma DECIMAL,
	contract VARCHAR(32),
	nr_contr VARCHAR(32),
	data_contr DATE,
	ind INT,
	stare VARCHAR(32),
	data_stare DATE,
	transe INT,
	cod_cs INT,
	matricola INT,
	modif VARCHAR(32),
	FOREIGN KEY (titular_id) REFERENCES titular(id),
	FOREIGN KEY (cimitir_id) REFERENCES cimitir(id),
	FOREIGN KEY (scutit_id) REFERENCES scutit(id)
);
